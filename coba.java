public class coba{
    public static void main(String[]args){
        double a = 7;
        double b = 5;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println("increment"+ a++);
        System.out.println("increment"+ a);
        System.out.println("decrement"+ --a);
        System.out.println(b++);
        System.out.println(b--);
        System.out.println((a*(a+b)/a)+b);


    }
}